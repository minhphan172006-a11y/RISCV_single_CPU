module Instruction_Memory(
    input  [31:0] addr,
    output [31:0] instruction
);
    reg [31:0] mem [0:63];

    assign instruction = mem[addr[31:2]];

    initial begin
        // Setup: x1 = 12, x2 = 5
        mem[0]  = {12'd12, 5'b00000, 3'b000, 5'b00001, 7'b0010011}; // addi x1, x0, 12
        mem[1]  = {12'd5,  5'b00000, 3'b000, 5'b00010, 7'b0010011}; // addi x2, x0, 5

        // R-type
        mem[2]  = {7'b0000000, 5'b00010, 5'b00001, 3'b000, 5'b00011, 7'b0110011}; // add  x3, x1, x2
        mem[3]  = {7'b0100000, 5'b00010, 5'b00001, 3'b000, 5'b00100, 7'b0110011}; // sub  x4, x1, x2
        mem[4]  = {7'b0000000, 5'b00010, 5'b00001, 3'b111, 5'b00101, 7'b0110011}; // and  x5, x1, x2
        mem[5]  = {7'b0000000, 5'b00010, 5'b00001, 3'b110, 5'b00110, 7'b0110011}; // or   x6, x1, x2
        mem[6]  = {7'b0000000, 5'b00010, 5'b00001, 3'b100, 5'b00111, 7'b0110011}; // xor  x7, x1, x2
        mem[7]  = {7'b0000000, 5'b00010, 5'b00001, 3'b001, 5'b01000, 7'b0110011}; // sll  x8, x1, x2
        mem[8]  = {7'b0000000, 5'b00010, 5'b00001, 3'b101, 5'b01001, 7'b0110011}; // srl  x9, x1, x2
        mem[9]  = {7'b0100000, 5'b00010, 5'b00001, 3'b101, 5'b01010, 7'b0110011}; // sra  x10, x1, x2
        mem[10] = {7'b0000000, 5'b00010, 5'b00001, 3'b010, 5'b01011, 7'b0110011}; // slt  x11, x1, x2

        // I-type ALU
        mem[11] = {12'd5,  5'b00001, 3'b000, 5'b01100, 7'b0010011}; // addi x12, x1, 5
        mem[12] = {12'd10, 5'b00001, 3'b110, 5'b01101, 7'b0010011}; // ori  x13, x1, 10

        // Previously-missing I-type ops (now fixed)
        mem[13] = {12'd10, 5'b00001, 3'b100, 5'b01110, 7'b0010011}; // xori  x14, x1, 10  -> 12^10=6
        mem[14] = {12'd20, 5'b00001, 3'b010, 5'b01111, 7'b0010011}; // slti  x15, x1, 20  -> 1
        mem[15] = {12'hFFF, 5'b00001, 3'b011, 5'b10000, 7'b0010011}; // sltiu x16, x1, -1 -> 1 (12 <u 0xFFFFFFFF)
        mem[16] = {12'd2,  5'b00001, 3'b001, 5'b10001, 7'b0010011}; // slli  x17, x1, 2   -> 48
        mem[17] = {12'd1,  5'b00001, 3'b101, 5'b10010, 7'b0010011}; // srli  x18, x1, 1   -> 6
        mem[18] = {7'b0100000, 5'b00001, 5'b00001, 3'b101, 5'b10011, 7'b0010011}; // srai x19, x1, 1 -> 6
        mem[19] = {7'b0000000, 5'b00010, 5'b00001, 3'b011, 5'b10100, 7'b0110011}; // sltu  x20, x1, x2 -> 0 (12<5? no)

        mem[20] = 32'b0; // NOP
        mem[21] = 32'b0;
    end
endmodule
